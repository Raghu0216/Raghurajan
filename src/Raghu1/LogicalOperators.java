package Raghu1;

public class LogicalOperators {
	public static void main(String[] args) {
		int a=10;
		int b=20;

		boolean c=(a<b) != (a>b);
		System.out.println("The Sum of Value are equal " +  c);
		int d=30;
		int e=20;

		boolean f=(d<e) && (d>e);
		System.out.println("The Sum of Value are equal " +  f);
		int g=7;
		int h=8;

		boolean i=(g<h) || (g>h);
		System.out.println("The Sum of Value are equal " +  i);
		
		
	}

}
